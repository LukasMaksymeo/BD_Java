    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        if(PasswordConf.getText().equals(Password.getText())){            
            try{
                String url = "jdbc:mysql://localhost:3306/extranathan";
                Connection con = DriverManager.getConnection(url, "root", null);
                Statement stmt = con.createStatement();
                stmt.executeUpdate("insert into admins (Nome,Senha,LastModifiedDate) values ('"+CampoNome.getText()+"','"
                                                      +PasswordConf.getText()+"','"
                                                      +LocalDate.now()+"');");           
                CampoNome.setText("");
                Password.setText("");
                PasswordConf.setText("");
                Cadastrou.setText("Criado com sucesso!!!");
            }
            catch(Exception ex){
                System.out.printf("Nao foi possivel conectar\n");
            }
        }
        else{
            Cadastrou.setText("Senhas n√£o condizem");
        }
    }//GEN-LAST:event_CadastrarActionPerformed

